package net.gotev.swapi.models

/**
 * Enumeration of all the character's eye colors.
 */
enum class EyeColor(val rawValue: String) {
    Black(rawValue = "black"),
    Blue(rawValue = "blue"),
    BlueGray(rawValue = "blue-gray"),
    Brown(rawValue = "brown"),
    Dark(rawValue = "dark"),
    Gold(rawValue = "gold"),
    Green(rawValue = "green"),
    Hazel(rawValue = "hazel"),
    Orange(rawValue = "orange"),
    Pink(rawValue = "pink"),
    Red(rawValue = "red"),
    Unknown(rawValue = "unknown"),
    White(rawValue = "white"),
    Yellow(rawValue = "yellow")
}
